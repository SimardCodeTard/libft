#include "../tests.h"

void	print_strs(char **strs)
{
	size_t	i;

	i = 0;
	printf("{ ");
	while (strs[i])
	{
		printf("\"%s\"", strs[i]);
		printf(", ");
		i++;
	}
	printf("NULL }");
}
